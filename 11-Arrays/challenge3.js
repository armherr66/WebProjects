const calcAverageHumanAge = ages =>
    ages.map(dog => dog <= 2 ? dog * 2 : 16 + dog * 4)
        .filter(age => age >= 18)
        .reduce((acc, age, i, arr) => acc + age / arr.length, 0);

console.log(calcAverageHumanAge([5, 2, 4, 1, 15, 8, 3]));